#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>

/**
* op_add - Entry point
* @a: int parameter
* @b: int parameter
* Return: int
*/
int op_add(int a, int b) {return (a + b); }

/**
* op_sub - Entry point
* @a: int parameter
* @b: int parameter
* Return: int
*/
int op_sub(int a, int b) {return (a - b); }

/**
* op_mul - Entry point
* @a: int parameter
* @b: int parameter
* Return: int
*/
int op_mul(int a, int b) {return (a * b); }

/**
* op_div - Entry point
* @a: int parameter
* @b: int parameter
* Return: int
*/
int op_div(int a, int b) {return (a / b); }

/**
* op_mod - Entry point
* @a: int parameter
* @b: int parameter
* Return: int
*/
int op_mod(int a, int b) {return (a % b); }
